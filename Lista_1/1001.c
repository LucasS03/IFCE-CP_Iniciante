#include <stdio.h>

int main() {

    int a, b, soma;

    scanf("%d", &a); // Leio o primeiro valor de a
    scanf("%d", &b); // Leio o segundo valor de b

    soma = a + b; // calculo a soma e guardo na variavel soma
    printf("X = %d\n", soma); // printa "X = " concatenada ao valor da variavel soma

    return 0;
}
