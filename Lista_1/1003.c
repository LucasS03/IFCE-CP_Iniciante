#include <stdio.h>

int main() {

    int a, b, soma;

    scanf("%d", &a); // Leio o valor de a
    scanf("%d", &b); // Leio o valor de b

    soma = a + b; // calculo a soma e guardo na variavel soma
    printf("SOMA = %d\n", soma); // printa "SOMA = " concatenada ao valor da variavel soma

    return 0;
}
