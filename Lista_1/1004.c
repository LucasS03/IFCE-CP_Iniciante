#include <stdio.h>

int main() {

    int a, b, prod;

    scanf("%d", &a); // Leio o valor de a
    scanf("%d", &b); // Leio o valor de b

    prod = a * b; // calculo o produto e guardo em prod
    printf("PROD = %d\n", prod); // printa "PROD = " concatenada ao valor da variavel prod

    return 0;
}
